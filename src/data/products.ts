// Helper functions
export function getProductBySlug(slug: string): Product | undefined {
  return products.find(p => p.slug === slug);
}

export function getFeaturedProducts(): Product[] {
  return products.filter(p => p.isFeatured);
}

export function getEcoProducts(): Product[] {
  return products.filter(p => p.category === 'eco');
}

export function getStandardProducts(): Product[] {
  return products.filter(p => p.category === 'standard');
}

export function getBestsellers(): Product[] {
  return products.filter(p => p.isBestseller);
}

export function getNewProducts(): Product[] {
  return products.filter(p => p.isNew);
}

export function getRelatedProducts(productId: string, limit = 4): Product[] {
  const product = products.find(p => p.id === productId);
  if (!product) return [];
  
  return products
    .filter(p => p.id !== productId && (p.category === product.category || p.material.type === product.material.type))
    .slice(0, limit);
}

export function searchProducts(query: string): Product[] {
  const lowercaseQuery = query.toLowerCase();
  return products.filter(p => 
    p.name.toLowerCase().includes(lowercaseQuery) ||
    p.shortDescription.toLowerCase().includes(lowercaseQuery) ||
    p.tags.some(t => t.toLowerCase().includes(lowercaseQuery))
  );
}
